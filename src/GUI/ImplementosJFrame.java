    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            Integer cantidad = Integer.parseInt(txtCantidad1.getText());
            String cliente = txtDNI.getText();
            String producto1 = cbxProducto1.getSelectedItem().toString();
            
            LocalDateTime datetime = LocalDateTime.now();
            Instant instant = datetime.atZone(ZoneId.systemDefault()).toInstant();
            //Instant instant = Timestamp.valueOf(localDateTime.format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"))).toInstant();
            Date date = Date.from(instant);
            
            Comprobante comprobante = new Comprobante();
            
            comprobante.setCantidad(cantidad);
            comprobante.setFecha(date);
            comprobante.setCliente(cliente);
            comprobante.setProducto(producto1);
            
            //Actualizar stock
            VO.Producto producto = productoDAO.buscarProducto(cbxProducto1.getSelectedItem().toString());
            int id = producto.getIdProducto();
            int stock1 = (producto.getStock()- cantidad);
            
            Producto pr = new Producto();
            pr.setIdProducto(id);
            pr.setStock(stock1);
            
            productoDAO.actualizarStock(pr);
            
            comprobanteDAo.nuevo(comprobante);
            JOptionPane.showMessageDialog(null, "Alquiler registrado");
            
            limpiar();
                        
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

