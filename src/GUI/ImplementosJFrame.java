    private void llenarClientes(){
        
        ArrayList<Producto> lista = (ArrayList<Producto>) productoDAO.listaProducto();
        
        cbxProducto1.removeAllItems();
        cbxProducto2.removeAllItems();
        
        /*cbxProducto1.addItem("Seleccione...");
        cbxProducto2.addItem("Seleccione...");
        cbxProducto3.addItem("Seleccione...");*/
        
        for(int i = 0; i<lista.size();i++){
            cbxProducto1.addItem(lista.get(i).getNomProducto());
            cbxProducto2.addItem(lista.get(i).getNomProducto());
        }
        
    }
    
